// VALORES PRIMITIVOS

// Esto es un valor tipo numero, solo es necesario escribir lo números

40

// Para guardar textos es hacerlo entre comillas ""

"Tzuriel López Montaño"

// Los booleanos se definen solo con la palabra true or false

true
false

// Hay un tipo de valores llamados vacíos o empty, se usan para guardar partes de código donde se espera tener un valor en el futuro

null
undefined

// VALORES NO PRIMITIVOS O TIPO OBJETOS

// Array, se ponen entre llaves y separados por comas

[1,2,3]

// Objeto, se ponen entre corchetes, el nombre del objeto, dos puntos y su valor

{ nombre: "Tzuriel"}